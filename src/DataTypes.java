public class DataTypes {
    public static void main(String[] args) {
        // the most used 5 types of data

        // String
        String brand = "Dacia";
        String model = "1310";

        // we write in capital letters
        System.out.println(brand.toUpperCase());
        // we find the size of a string
        System.out.println(brand.length());

        // integer
        int manufacturingYear = 1989;

        // double
        double price = 3895.5;

        // boolean
        boolean registered = true;

        // char = a single character
        char grade = 'A';
    }
}
